marks = int(input("Please enter the marks obtained out of 100 :"))
if marks >= 90 :
    print("Grade = O, Outstanding result!!")
elif marks >= 80 :
    print("Grade = A, Great result.")
elif marks >= 70 :
    print("Grade = B, Good result!!")
elif marks >= 60 :
    print("Grade = C, You can acheive better results!!")
elif marks >= 50 :
    print("Grade = D, Buckle up!!")
elif marks >= 34 :
    print("Grade = E, Not Good!!")
else :
    print("Grade = F, You failed!! :(")